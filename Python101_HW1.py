import turtle
tao = turtle. Pen ()
tao.shape ('turtle')
tao.penup ()
tao.goto (-100,200)
tao.pendown ()

for i in range (2):
    tao.forward (200)
    tao.right (90)
    tao.forward (400)
    tao.right (90)

tao.left (45)
tao.forward (100)
tao.right (45)
tao.forward (100)
tao.forward (100)
tao.right (90)
tao.forward (400)
